/*
 == igualdade de valores

 === igualdade valores e tipos

 != diferenca de valores

 !== diferenca de valores e tipo

 > maior que

 < menor que
*/

let a = 10
let b = 10
let c = '10'
console.log(a == b );
console.log(a === c); //valor igual ,mas tipos diferentes
console.log(a == c); //valor igual , nao considera o tipo
console.log(a != c); // diferenca de valores apenas
console.log(a !== c); // verifica se ha diferenca entre valor e tipos




